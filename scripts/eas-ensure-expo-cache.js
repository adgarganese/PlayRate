#!/usr/bin/env node
/**
 * EAS Build: ensure .expo/web exists before prebuild; ensure ios/.xcode.env.local exists
 * so pod install does not hit Permission denied when creating/reading it.
 * Run by eas-build-pre-install in package.json.
 */
const fs = require('fs');
const path = require('path');

const cwd = process.cwd();

const dir = path.join(cwd, '.expo', 'web');
try {
  fs.mkdirSync(dir, { recursive: true });
} catch (e) {
  console.warn('[eas-ensure-expo-cache] mkdir .expo/web failed:', e.message);
}

// EAS node path (pod install expects this when .xcode.env.local is present)
const xcodeEnvLocal = path.join(cwd, 'ios', '.xcode.env.local');
const nodePath = '/Users/expo/.nvm/versions/node/v20.19.4/bin/node';
const content = `# Generated by eas-build-pre-install to avoid EAS permission errors
export NODE_BINARY=${nodePath}
`;
try {
  fs.writeFileSync(xcodeEnvLocal, content, { mode: 0o644 });
} catch (e) {
  console.warn('[eas-ensure-expo-cache] write ios/.xcode.env.local failed:', e.message);
}

// React Native autolinking writes to ios/build/generated/autolinking/autolinking.json during pod install.
// Create the dir so Ruby's FileUtils.mkdir_p doesn't hit EACCES on EAS.
const autolinkingDir = path.join(cwd, 'ios', 'build', 'generated', 'autolinking');
try {
  fs.mkdirSync(autolinkingDir, { recursive: true });
} catch (e) {
  console.warn('[eas-ensure-expo-cache] mkdir ios/build/generated/autolinking failed:', e.message);
}
